    private String konversiNilaiHuruf(double nilai) {
        if (nilai >= 85) return "A";
        else if (nilai >= 75) return "B";
        else if (nilai >= 65) return "C";
        else if (nilai >= 55) return "D";
        else return "E";
    }
    
     private void updateTable() {
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Clear semua data di tabel
        
        // Tambahkan data dari array ke tabel
        for (int i = 0; i < currentIndex; i++) {
            model.addRow(new Object[]{
                dataArray[i][0], // NIM
                dataArray[i][1], // UTS
                dataArray[i][2], // Tugas
                dataArray[i][3], // UAS
                dataArray[i][4], // Absensi
                dataArray[i][5], // Nilai Akhir
                dataArray[i][6]  // Nilai Huruf
            });
        }
    }
    
    private void initTable() {
        javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel(
            new Object[][] {}, // Data kosong
            new String[] {
                "NIM", 
                "UTS", 
                "Tugas", 
                "UAS", 
                "Absensi", 
                "Akhir", 
                "Huruf"
            }
        ) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; // Biar tidak bisa diedit
            }
        };
        
        jTable1.setModel(model);
    }

