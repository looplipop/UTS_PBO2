    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    // Validasi maksimal 5 data
        if (currentIndex >= 5) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                "Data sudah mencapai batas maksimal 5! Silahkan Anda mulai ulang aplikasi terlebih dahulu.");
            return;
        }

        // Ambil nilai dari textfield
        String nim = jTextField2.getText().trim();
        String uts = jTextField3.getText().trim();
        String tugas = jTextField4.getText().trim();
        String uas = jTextField5.getText().trim();
        String absensi = jTextField6.getText().trim();

        // Validasi NIM harus angka
        if (!nim.matches("\\d+")) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                "NIM harus berupa angka!");
            jTextField2.requestFocus();
            return;
        }

        // Validasi input tidak boleh kosong
        if (nim.isEmpty() || uts.isEmpty() || tugas.isEmpty() || 
            uas.isEmpty() || absensi.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                "Semua field harus diisi!");
            return;
        }

        // Validasi input nilai harus angka
        try {
            double nilaiUTS = Double.parseDouble(uts);
            double nilaiTugas = Double.parseDouble(tugas);
            double nilaiUAS = Double.parseDouble(uas);
            double nilaiAbsensi = Double.parseDouble(absensi);
            
            // Validasi range nilai (0-100)
            if (nilaiUTS < 0 || nilaiUTS > 100 || 
                nilaiTugas < 0 || nilaiTugas > 100 ||
                nilaiUAS < 0 || nilaiUAS > 100 || 
                nilaiAbsensi < 0 || nilaiAbsensi > 100) {
                javax.swing.JOptionPane.showMessageDialog(this,
                    "Nilai harus antara 0 - 100!");
                return;
            }
            
            // Hitung nilai akhir
            double nilaiAkhir = (nilaiUTS * 0.3) + (nilaiTugas * 0.3) + 
                               (nilaiUAS * 0.3) + (nilaiAbsensi * 0.1);
            String nilaiHuruf = konversiNilaiHuruf(nilaiAkhir);
            
            // Simpan data ke array
            dataArray[currentIndex][0] = nim;
            dataArray[currentIndex][1] = uts;
            dataArray[currentIndex][2] = tugas;
            dataArray[currentIndex][3] = uas;
            dataArray[currentIndex][4] = absensi;
            dataArray[currentIndex][5] = String.format("%.2f", nilaiAkhir);
            dataArray[currentIndex][6] = nilaiHuruf;
            
            currentIndex++;
            updateTable();
            clearForm();
            
            javax.swing.JOptionPane.showMessageDialog(this, 
                "Data berhasil diinput!\nData ke: " + currentIndex + " dari 5");
            
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                "Nilai UTS, Tugas, UAS, dan Absensi harus angka!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

